package com.tns.day4;

public class OopConceptDemo {
	private int Id;
	private String Name;
	private int Age;
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getAge() {
		return Age;
	}
	public void setAge(int age) {
		Age = age;
	}
	@Override
	public String toString() {
		return "OopConceptDemo [Id=" + Id + ", Name=" + Name + ", Age=" + Age + "]";
	}
	
	
	
	

}
