package com.tns.day3;

public class SwitchCase {

	public static void main(String[] args) {
		
		for(int i=1;i<5;i++) {
		switch(i) {
		
		case 1:System.out.println(" Persion 1: hello");
		break;
		case 2:System.out.println(" Persion 2: hii");
		break;
		case 3:System.out.println(" Persion 1: Im onkar");
		break;
		case 4:System.out.println(" Persion 2: Im shreya");
		break;
		}
		}
	}

}
